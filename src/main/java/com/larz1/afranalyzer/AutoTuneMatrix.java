package com.larz1.afranalyzer;

public class AutoTuneMatrix {
    /**
     *
     *  Autotune Matrix
     *  +----------+
     *  |tlc tc trc|
     *  |lc  cc rc |
     *  |blc bc brc|
     *  +----------+
     *
     */
    double tlc; // top left cell
    double tc; // top cell
    double trc; // top right cell
    double lc; //left cell
    double cc; // center cell
    double rc; //right cell
    double blc; // bottom left cell
    double bc; // bottom cell
    double brc; // bottom right cell
}
